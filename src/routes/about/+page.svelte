

    <h1 style="font-size: 36px; font-weight: bold margin:500;">About MealBridge</h1>
    <p><strong>MealBridge</strong> is a platform that connects food donors with people in need, ensuring that surplus food is distributed to shelters, NGOs, and individuals facing food insecurity.</p>

    <h2 style="font-size: 18px; font-weight: bold;" >Our Mission</h2>
    <p>We aim to reduce food waste and hunger by making food donation simple, efficient, and accessible to everyone.</p>

    <h2 style="font-size: 18px; font-weight: bold;">How It Works</h2>
    <ul>
        <li><strong>Donors</strong>: List surplus food available for donation.</li>
        <li><strong>Recipients</strong>: Browse available food and request what they need.</li>
        <li><strong>Distribution</strong>: Donors and recipients coordinate for pickup or delivery.</li>
    </ul>

